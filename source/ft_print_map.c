#include "../bsq.h"

void	put_char(char c)
{
	write(1, &c, 1);
}

void	ft_print_map(char **map)
{
	int x;
	int y;

	y = 0;
	while (map[y] != 0)
	{
		x = 0;
		while (map[y][x] != '\0')
		{
			put_char(map[y][x]);
			x++;
		}
		put_char('\n');
		y++;
	}
}
